#include "libft.h"

char	*ft_strrev(char *str)
{
	char	tmp;
	size_t	size;
	size_t	i;

	i = 0;
	size = ft_strlen(str);
	while (i < size / 2)
	{
		tmp = str[i];
		str[i] = str[size - i - 1];
		str[size - i - 1] = tmp;
		i++;
	}
	return (str);
}
