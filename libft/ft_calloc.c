#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*result;

	if (size && count > (size_t)-1 / size)
		return (NULL);
	result = (void *)malloc(count * size);
	if (result == NULL)
		return (NULL);
	ft_bzero(result, count * size);
	return (result);
}
